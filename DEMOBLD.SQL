DROP TABLE IF EXISTS ORDERS;
CREATE TABLE ORDERS(
	ORDERID INTEGER AUTO_INCREMENT,
    CUSTOMERID INTEGER , FOREIGN KEY (CUSTOMERID) REFERENCES CUSTOMERS(CUSTOMERID)
    ON DELETE CASCADE  ON UPDATE CASCADE,
    PRODUCTID INTEGER , FOREIGN KEY (PRODUCTID) REFERENCES PRODUCTS(PRODUCTID)
    ON DELETE CASCADE  ON UPDATE CASCADE,
    ORDERSTATUS boolean default false,
    PRODUCTQTY FLOAT(4,2),
    ORDERDATE DATE NOT NULL DEFAULT (CURRENT_DATE) ,
    CONSTRAINT ORDERID_PRIMARY_KEY PRIMARY KEY (ORDERID)
)ENGINE =InnoDB AUTO_INCREMENT=10000;


